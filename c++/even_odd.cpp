//Write a program to check whether a number is Even or Odd program
#include <iostream>
using namespace std;

int main()
{
    int num;
    cout<<"enter the value of n"<<endl;
    cin>>num;
    if(num%2==0)
    {
        cout<<"Even Number"<<endl;
        
    }else{
        cout<<"Odd Number"<<endl;
    }
    
    return 0;
}
